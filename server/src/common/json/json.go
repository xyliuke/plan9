type JSONObject struct {
	data interface{}
}

//通过byte数组生成一个json对象
func NewJSONObject(body []byte) (*JSONObject, error) {
	j := new(JSONObject)
	err := j.UnmarshalJSONObject(body)
	if err != nil {
		return nil, err
	}
	return j, nil
}
//字符串生成json对象
//body 需要解析的字符串
//返回json对象和是否出错
func NewJSONObjectByString(body string) (*JSONObject, error) {
	b := []byte(body)
	return NewJSONObject(b)
}

// New returns a pointer to a new, empty `JSONObject` object
func New() *JSONObject {
	return &JSONObject{
		data: make(map[string]interface{}),
	}
}

// Interface returns the underlying data
func (j *JSONObject) Interface() interface{} {
	return j.data
}

// Encode returns its marshaled data as `[]byte`
func (j *JSONObject) Encode() ([]byte, error) {
	return j.MarshalJSONObject()
}

// EncodePretty returns its marshaled data as `[]byte` with indentation
func (j *JSONObject) EncodePretty() ([]byte, error) {
	return json.MarshalIndent(&j.data, "", "  ")
}

// Implements the JSONObject.Marshaler interface.
func (j *JSONObject) MarshalJSONObject() ([]byte, error) {
	return json.Marshal(&j.data)
}

// Set modifies `JSONObject` map by `key` and `value`
// Useful for changing single key/value in a `JSONObject` object easily.
func (j *JSONObject) Set(key string, val interface{}) {
	m, err := j.Map()
	if err != nil {
		return
	}
	m[key] = val
}

// SetPath modifies `JSONObject`, recursively checking/creating map keys for the supplied path,
// and then finally writing in the value
func (j *JSONObject) SetPath(branch []string, val interface{}) {
	if len(branch) == 0 {
		j.data = val
		return
	}

	// in order to insert our branch, we need map[string]interface{}
	if _, ok := (j.data).(map[string]interface{}); !ok {
		// have to replace with something suitable
		j.data = make(map[string]interface{})
	}
	curr := j.data.(map[string]interface{})

	for i := 0; i < len(branch)-1; i++ {
		b := branch[i]
		// key exists?
		if _, ok := curr[b]; !ok {
			n := make(map[string]interface{})
			curr[b] = n
			curr = n
			continue
		}

		// make sure the value is the right sort of thing
		if _, ok := curr[b].(map[string]interface{}); !ok {
			// have to replace with something suitable
			n := make(map[string]interface{})
			curr[b] = n
		}

		curr = curr[b].(map[string]interface{})
	}

	// add remaining k/v
	curr[branch[len(branch)-1]] = val
}

// Del modifies `JSONObject` map by deleting `key` if it is present.
func (j *JSONObject) Del(key string) {
	m, err := j.Map()
	if err != nil {
		return
	}
	delete(m, key)
}

// Get returns a pointer to a new `JSONObject` object
// for `key` in its `map` representation
//
// useful for chaining operations (to traverse a nested JSONObject):
//    js.Get("top_level").Get("dict").Get("value").Int()
func (j *JSONObject) Get(key string) *JSONObject {
	m, err := j.Map()
	if err == nil {
		if val, ok := m[key]; ok {
			return &JSONObject{val}
		}
	}
	return &JSONObject{nil}
}

// GetPath searches for the item as specified by the branch
// without the need to deep dive using Get()'s.
//
//   js.GetPath("top_level", "dict")
func (j *JSONObject) GetPath(branch ...string) *JSONObject {
	jin := j
	for _, p := range branch {
		jin = jin.Get(p)
	}
	return jin
}

// GetIndex returns a pointer to a new `JSONObject` object
// for `index` in its `array` representation
//
// this is the analog to Get when accessing elements of
// a JSONObject array instead of a JSONObject object:
//    js.Get("top_level").Get("array").GetIndex(1).Get("key").Int()
func (j *JSONObject) GetIndex(index int) *JSONObject {
	a, err := j.Array()
	if err == nil {
		if len(a) > index {
			return &JSONObject{a[index]}
		}
	}
	return &JSONObject{nil}
}

// CheckGet returns a pointer to a new `JSONObject` object and
// a `bool` identifying success or failure
//
// useful for chained operations when success is important:
//    if data, ok := js.Get("top_level").CheckGet("inner"); ok {
//        log.Println(data)
//    }
func (j *JSONObject) CheckGet(key string) (*JSONObject, bool) {
	m, err := j.Map()
	if err == nil {
		if val, ok := m[key]; ok {
			return &JSONObject{val}, true
		}
	}
	return nil, false
}

// // Map type asserts to `map`
func (obj *JSONObject) Map() (map[string]interface{}, error) {
	if m, ok := (obj.data).(map[string]interface{}); ok {
		return m, nil
	}
	return nil, errors.New("type assertion to map[string]interface{} failed")
}

// Array type asserts to an `array`
func (j *JSONObject) Array() ([]interface{}, error) {
	if a, ok := (j.data).([]interface{}); ok {
		return a, nil
	}
	return nil, errors.New("type assertion to []interface{} failed")
}

// Bool type asserts to `bool`
func (j *JSONObject) Bool() (bool, error) {
	if s, ok := (j.data).(bool); ok {
		return s, nil
	}
	return false, errors.New("type assertion to bool failed")
}

// String type asserts to `string`
func (j *JSONObject) String() (string, error) {
	if s, ok := (j.data).(string); ok {
		return s, nil
	}
	return "", errors.New("type assertion to string failed")
}

// Bytes type asserts to `[]byte`
func (j *JSONObject) Bytes() ([]byte, error) {
	if s, ok := (j.data).(string); ok {
		return []byte(s), nil
	}
	return nil, errors.New("type assertion to []byte failed")
}

// StringArray type asserts to an `array` of `string`
func (j *JSONObject) StringArray() ([]string, error) {
	arr, err := j.Array()
	if err != nil {
		return nil, err
	}
	retArr := make([]string, 0, len(arr))
	for _, a := range arr {
		if a == nil {
			retArr = append(retArr, "")
			continue
		}
		s, ok := a.(string)
		if !ok {
			return nil, err
		}
		retArr = append(retArr, s)
	}
	return retArr, nil
}

// MustArray guarantees the return of a `[]interface{}` (with optional default)
//
// useful when you want to interate over array values in a succinct manner:
//		for i, v := range js.Get("results").MustArray() {
//			fmt.Println(i, v)
//		}
func (j *JSONObject) MustArray(args ...[]interface{}) []interface{} {
	var def []interface{}

	switch len(args) {
	case 0:
	case 1:
		def = args[0]
	default:
		log.Panicf("MustArray() received too many arguments %d", len(args))
	}

	a, err := j.Array()
	if err == nil {
		return a
	}

	return def
}

